		<ul id="conditions">
            	<li>
                Heart Disease
                	<ul>
                    	<li>
                        Patient optimally treated with diuretics and vasodilators</li>
                        <li>Significant or recurrent symptoms of CHF or other cardiac disease</li>	<li>Patient has decreased ability to carry on activity without SOB or discomfort<br></li><li><b>Supportive History:</b><span
  style='mso-spacerun:yes'>  </span>MI, Arrest, Syncope, CVA, Recurrent Super
  ventricular arrhythmias
  						</li>
                    </ul>
               </li>
               <li>
               Cancer
               	<ul>
                	<li>
                    	Any metastatic disease that is inoperable or aggressive treatment is complete</li>
                 </ul>
                 </li>
                 <li>
                 Pulmonary Disease
                 	<ul>
                    	<li>
                        	Disabling dyspnea at rest, unresponsive to bronchodilators, resulting in decreased functional capacity.(bed to chair existence)</li>
                        <li>Progression of end stage disease as evidenced by increasing visits to the MD office, ER visits, or 2-3 admissions to the Hospital in 1 Yr</li>
                        <li>Patient utilizes home oxygen</li>
                        
                        <li> <b>Supportive History:</b> Patient has Right Heart Failure, Wt loss greater than 10% of body weight over 6 months. Resting Tachycardia</li>
                        </ul>
                        </li>
                <li>
                Cerebrovascular Accident
                	<ul>
                    	 <ul><b>Active Phase</b>
                                <li>
                                	 In coma or persistent vegetative state for more than 3 days</li>
                                <li>Obtunded beyond 3 days past the event</li>
                                <li>Has dysphagia and is not receiving artificial nutrition</li>
                           </ul>
                           <ul><b>Chronic Phase</b>
                           		<li>
                                	Increased assistance with ADL's
                                </li>
                                <li>Unable to ambulate without assistance</li>
                                <li>Decreased verbal communication</li>
                                <li>Incontinence of bowel or bladder (intermittentent or constant)</li>
                                </ul>
                                
                       </ul>
                       <li>
                       		Dimentia
                            <ul>
                            	<li>
                                	Stage 7 or higher or FAST scale
                                </li>
                                <li> Requires assistance with ADL's</li>
                                <li>No consistent meaningful communication, just repetative, canned phrases</li>
                                <li>Incontinence of bowel or bladder (intermittentent or constant)</li></ul>
                                <ul><b>Last 12 Months: (One of the following)</b>
                                <li>Required Tx for pneumonia, UTI, Septicemia, Stage 3-4 Decubitus, Fever recurrent after treatment. Unable to maintain sufficient nutritional intake. Compilcate bomorbid phase conditions.</li>
                               </ul>
                     <li>Failure to Thrive
                     	<ul>
                        	<li>Frequent ER visits, MD visits or Acute Admissions</li>
                            <li>Weight loss indicated by BMI of 22Kg/m&sup2;</li>
                            <li>Declined despite nutrional supplements</li>
                            <li>Karnofsky or Palliagive Performance Scale =< 40%</li>
                            <li>Dependence on ADL care   </li>
                            
                         </ul>
                     </li>
                     <li>Renal Disease
                     	<ul>
                        	<li>Not on dialysis</li>
                            <li>Signifigant functional decline</li>
                            <li>Signifigant bomorbid conditions</li>
                         </ul>
                     </li>
                     <li>Liver Disease
                     	<ul>
                        	<li>May be on transplant list and still receive hospice</li>
                            <li>Refractory ascites, Jaundice, Malnutrition and muscle wasting</li>
                            <li>PT INR > 1.5 AND Serum Albumin < 2.5gm/dl</li>
                        </ul>
                     </li>
                     <li>HIV Disease
                     	<ul>
                        	<li>Most recent CD4 count must be < 25 cells/mcL) <b>OR</b> Viral load greater than 100,000</li>
                            <li>Must have at least one:  Lymphoma, not responsive to Tx, Wasting, MAC   bactremia, Kaposi sarcoma unresponsive to tx, renal failure, toxoplasmosis</li>
                            <li>Functional Performance Scale of 50% or less</li>
                         </ul>
                     </li>
           </ul>
